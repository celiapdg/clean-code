/**
 * Duplicaciones - resultado de no aplicar el principio DRY
 * 
 * Hay dos tipos de duplicidad: 
 * REAL - el código es idéntico y cumple la misma función
 * - un cambio implica hacer los mismos cambios en los demás sitios
 * - mayor probabilidad de error humano
 * - mayor cantidad de pruebas innecesarias
 * 
 * ACCIDENTAL - el código luce similar pero cumple tareas distinas
 * cuando solo se modifica un lugar. Se puede trabajar con parámetros 
 * y/u optimizaciones
 */